class Profile {
  final String name;
  final String position;
  final String department;
  final String email;
  final String phone;
  final String location;
  final String image;

  Profile({
    required this.name,
    required this.position,
    required this.department,
    required this.email,
    required this.phone,
    required this.location,
    required this.image,
  });
}